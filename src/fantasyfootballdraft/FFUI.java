    private static String getLastEntered(){
        String line = "";
        if(previousEntered.size() > 0){
            line = previousEntered.get(previousEntered.size() - 1);
        }
        return line;
    }
            
    public static void initalizePlayerDropDown() throws Exception{
        ResultSet rs = sqlUtils.readDataBase();
        while(rs.next()){
            String position = rs.getString("position");
            position = position.equalsIgnoreCase("FB") ? "RB" : position;
            jComboBox1.addItem(rs.getString("name") + "  " + position
                    + "  " + rs.getString("teamname") + "  " + rs.getString("byeweek"));            
        }
    }
            
    public static void initalizeDefenseDropDown() throws Exception{
        ResultSet rs = sqlUtils.readTeamsDataBase();
        while(rs.next()){
            jComboBox1.addItem(rs.getString("name") + "  " + "DST"
                    + "  " + "-" + "  " + rs.getString("bye_week"));            
        }
    }
    
    public static void removeFromDropDown(NFLPlayer np){
        jComboBox1.removeItem(np.getName() + "  " + np.getPosition()
                + "  " + np.getTeam() + "  " + np.getByeWeek());
    }
                
