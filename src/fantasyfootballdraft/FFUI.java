    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        printout("");
        NFLPlayer np = new NFLPlayer(jComboBox1.getSelectedItem().toString());
        String amount = jTextField1.getText().trim();        
        Player p = findCorrectPlayer(jComboBox2.getSelectedItem().toString().toLowerCase());
        String rosterSpot = p.findRosterSpot(np.getPosition());
        if(p.handleBid(Integer.parseInt(amount)) && !rosterSpot.equals("")){
            updateRosters(rosterSpot, np);
            removeFromDropDown(np);
            p.increaseTotalNumOnRoster(np.getPosition());
            startUp.getNextPlayerToBid(); 
        }
        else{
            p.undoRosterSpot(np.getPosition());
        }
    }//GEN-LAST:event_jButton1MouseClicked

